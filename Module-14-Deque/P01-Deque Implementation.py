from collections import deque
print("\nProgram 1!!!\n")
d = deque()
print(d)

d.append(10)
print(d)

d.append(20)
d.append(30)
print(d)

d.appendleft(40)
print(d)

print(d.pop())
print(d.popleft())
print(d)
print("\n!!!End of Program 1!!!\n")

